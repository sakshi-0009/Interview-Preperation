/* Write a program to create an array of 5 Integer elements and print all 5 elements from an array.(take hardcoded values) */

package Array_Assignment_01;

class Program01 {
    public static void main(String[] args) {
        int arr[] = new int[]{10,20,30,40,50};
        for(int i=0;i<5;i++){
            System.out.println(arr[i]);
        }
    }
}
